  var bg = document.querySelector(".imgg");
  var allaudio = document.querySelector(".list");
  var audio = new Audio();
  var selectedSongIndex = 0;
  var back = document.querySelector("#backward");
  var play = document.querySelector("#play");
  var forward = document.querySelector("#forward");


  function mainfun() {
    var card = "";
    arr.forEach(function (val, index) {
      card += `<div id=${index} class="list-right">
                  <img src="${val.image}" alt="">
                  <h5 class="font">${val.name}</h5>
                  <p class="font">${val.time}</p>
              </div>`;
    });
    allaudio.innerHTML = card;
    audio.src = arr[selectedSongIndex].url;
    bg.style.backgroundImage = `url(${arr[selectedSongIndex].image})`;
  }
  
  mainfun();
  
  allaudio.addEventListener("click", function (event) {
    if (event.target && event.target.nodeName == "DIV") {
      selectedSongIndex = parseInt(event.target.id);
      play.innerHTML =`<i class="ri-pause-fill"></i>`
      flag = 1;
      mainfun();
      audio.play();
    }
  });
  

var flag = 0;
  play.addEventListener("click",function(){
    if(flag == 0){
       play.innerHTML =`<i class="ri-pause-fill"></i>`
       mainfun();
       audio.play();
       flag = 1;
    }else{
        play.innerHTML =`<i class="ri-play-fill"></i>`
        mainfun();
        audio.pause();
        flag = 0;
    }
  })

  forward.addEventListener("click",function(){
    if(selectedSongIndex <= arr.length -1){
        selectedSongIndex++
        mainfun();
        audio.play();
    }else{
        forward.style.opacity = 0.5;
    }
  })
  backward.addEventListener("click",function(){
    if(selectedSongIndex > 0){
        selectedSongIndex--
        mainfun();
        audio.play();
    }else{
        backward.style.opacity = 0.5; 
    }
  })